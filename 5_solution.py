# Weather Activity Suggestion
#sunny - go for walk , rainy - read a book , snowy - make a snow man
weather=input("Enter your surrounding\'s weather: ")
weather=weather.capitalize()
if (weather == "Sunny"):
    print("Go for a walk")
elif (weather == "Rainy"):
    print("Stay home and read a book")
elif (weather == "Snowy"):
    print("Make a Snow Man")